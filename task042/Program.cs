int number = 1;
int counter = 0;
bool IsError = true;
Console.WriteLine("Введите числа. Чтобы выйти из программы, введите 0");
while (number != 0)
{
    while (IsError)
    {
        string numberStr = Console.ReadLine();
        if (int.TryParse(numberStr, out number)) IsError = number != 0;
        if (number > 0)
            counter++;
    }
}
Console.WriteLine($"Количество введенных с консоли чисел больше 0: {counter}");

