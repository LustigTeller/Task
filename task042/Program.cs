// Задача 42. Определить сколько чисел больше 0 введено с клавиатуры

/*
    -   просим пользователя ввести числа с консоли
    -   считываем и переводим из стринга в интовые значения
    -   создать счетчик для чисел > 0 
*/

//вышло два варианта. посмотрите пожалуйста оба!

int number = 1;
int counter = 0;
Console.WriteLine("Введите числа. Чтобы выйти из программы, введите 0 или любую букву");
while (number != 0)
{
    string numberStr = Console.ReadLine();
    int.TryParse(numberStr,out number);
    if (number > 0)
        counter++;
}
Console.WriteLine($"Количество введенных с консоли числе больше 0: {counter}");


